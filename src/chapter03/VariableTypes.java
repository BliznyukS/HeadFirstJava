package chapter03;

public class VariableTypes {
    public static void main(String[] args) {

        char c = '1';
        boolean t = true;
        boolean f = false;
        byte b = 127;
        short s = 32767;
        int i = 2147483647;
        long l = 3656456799999999999L;
        float fl = 3.4f;
        double d = 5.0;

        int sum = (int) c + i;
        System.out.println(sum);

    }
}
